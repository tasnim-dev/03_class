<?php 

# Weekly Message using switch case statemnet

$day = "Monday";

switch($day) {
    case 'Sunday':
        echo $day." : Wishing you a great day!";
        break;

    case 'Monday':
        echo $day." : May you be loved more and more everyday.";
        break;

    case 'Tuesday':
        echo $day." : May you have a great day today, sparkle and shine amongst everyone!";
        break;

    case 'Wednesday':
        echo $day." : Reminder: It's you and you are unique. Have a great day!";
        break;

    case 'Thursday':
        echo $day." : Wishing you wealth and good health, have a great day.";
        break;

    case 'Friday':
        echo $day." : Positive anything is better than negative nothing";
        break;

    case 'Saturday':
        echo $day." : Always find opportunities to make someone smile";
        break;

    case 'Sunday':
        echo "Do not let yesterday' stress ruin your today.";

} 

?>